package djjy.djzx;

import java.util.List;

public class Cons {

    private int code;
    private String message;
    private DataDTO data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public DataDTO getData() {
        return data;
    }

    public void setData(DataDTO data) {
        this.data = data;
    }

    public static class DataDTO {
        private int total;
        private List<RowsDTO> rows;

        public int getTotal() {
            return total;
        }

        public void setTotal(int total) {
            this.total = total;
        }

        public List<RowsDTO> getRows() {
            return rows;
        }

        public void setRows(List<RowsDTO> rows) {
            this.rows = rows;
        }

        public static class RowsDTO {
            private int id;
            private int act_id;
            private String news_title;
            private String news_content;
            private String news_covering_path;
            private Object org_id;
            private int news_score;
            private String remark;
            private String create_time;
            private String update_time;

            public int getId() {
                return id;
            }

            public void setId(int id) {
                this.id = id;
            }

            public int getAct_id() {
                return act_id;
            }

            public void setAct_id(int act_id) {
                this.act_id = act_id;
            }

            public String getNews_title() {
                return news_title;
            }

            public void setNews_title(String news_title) {
                this.news_title = news_title;
            }

            public String getNews_content() {
                return news_content;
            }

            public void setNews_content(String news_content) {
                this.news_content = news_content;
            }

            public String getNews_covering_path() {
                return news_covering_path;
            }

            public void setNews_covering_path(String news_covering_path) {
                this.news_covering_path = news_covering_path;
            }

            public Object getOrg_id() {
                return org_id;
            }

            public void setOrg_id(Object org_id) {
                this.org_id = org_id;
            }

            public int getNews_score() {
                return news_score;
            }

            public void setNews_score(int news_score) {
                this.news_score = news_score;
            }

            public String getRemark() {
                return remark;
            }

            public void setRemark(String remark) {
                this.remark = remark;
            }

            public String getCreate_time() {
                return create_time;
            }

            public void setCreate_time(String create_time) {
                this.create_time = create_time;
            }

            public String getUpdate_time() {
                return update_time;
            }

            public void setUpdate_time(String update_time) {
                this.update_time = update_time;
            }
        }
    }
}

package djjy.stlx;

import java.util.ArrayList;
import java.util.HashMap;

public class Topic {
    private String topic_title;
    private Integer topic_score;
    private String option_def;
    private String analysis;
    private ArrayList<HashMap<String,String>> options;
    //用户回答信息
    private HashMap<String,Object> answer;

    public HashMap<String, Object> getAnswer() {
        return answer;
    }

    public void setAnswer(HashMap<String, Object> answer) {
        this.answer = answer;
    }

    private Integer type;

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public String getTopic_title() {
        return topic_title;
    }

    public void setTopic_title(String topic_title) {
        this.topic_title = topic_title;
    }

    public Integer getTopic_score() {
        return topic_score;
    }

    public void setTopic_score(Integer topic_score) {
        this.topic_score = topic_score;
    }

    public String getOption_def() {
        return option_def;
    }

    public void setOption_def(String option_def) {
        this.option_def = option_def;
    }

    public String getAnalysis() {
        return analysis;
    }

    public void setAnalysis(String analysis) {
        this.analysis = analysis;
    }

    public ArrayList<HashMap<String,String>> getOptions() {
        return options;
    }

    public void setOptions(ArrayList<HashMap<String,String>> options) {
        this.options = options;
    }
}
